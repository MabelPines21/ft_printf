#include "libft.h"

void	ft_putstr_fd(char const *s, int fd)
{
	int i;

	if (!s || !fd)
		return ;
	if (fd >= 0)
	{
		i = 0;
		while (s[i] != '\0')
		{
			write(fd, &s[i], 1);
			i++;
		}
	}
}
